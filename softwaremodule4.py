# ---------- BackendV2 wrapper ----------

class NAPulseBackend(BackendV2):
    """
    BackendV2 wrapper around a neutral-atom-style processor.

    The `processor` object must provide:
      - num_qubits : int
      - load_circuit(qiskit_circuit)
      - generate_init_processor_state() -> qutip.Qobj
      - run_state(rho0) -> object with .states[-1] = final rho
      - get_final_circuit_state(res, renorm=False) -> qutip.Qobj

    This backend:
      * expects circuits already transpiled to ('rx','rz','cz')
      * simulates them with the processor
      * marginalizes onto the active qubits for counts
      * returns a Job whose result() is a qiskit.result.Result suitable for RB.
    """
    def __init__(self, processor, basis=("rx", "rz", "cz"), shots=4000):
        super().__init__(name="na_pulse")
        self.processor = processor
        self._basis = tuple(basis)
        self.set_options(shots=int(shots))

        # Build a simple Target describing native operations
        n = processor.num_qubits
        tgt = Target(num_qubits=n)

        rx_props = {(q,): InstructionProperties() for q in range(n)}
        rz_props = {(q,): InstructionProperties() for q in range(n)}
        cz_props = {
            (i, j): InstructionProperties()
            for i in range(n)
            for j in range(i + 1, n)
        }
        meas_props = {(q,): InstructionProperties() for q in range(n)}

        tgt.add_instruction(RXGate(0.0), rx_props)
        tgt.add_instruction(RZGate(0.0), rz_props)
        if cz_props:
            tgt.add_instruction(CZGate(), cz_props)
        tgt.add_instruction(Measure(), meas_props)

        self._target = tgt

    # ---- BackendV2 abstract bits ----
    @classmethod
    def _default_options(cls):
        return Options(shots=4000)

    @property
    def max_circuits(self):
        # No intrinsic limit
        return None

    @property
    def target(self):
        return self._target

    @property
    def num_qubits(self):
        return self.processor.num_qubits

    # ---- main run ----
    def run(self, circuits, **kwargs):
        """
        Simulate circuits and return a Job wrapping a qiskit Result.

        We:
          * use `shots` from kwargs or backend options,
          * for each circuit:
              - use the processor to get the final density matrix,
              - compute diagonal probabilities,
              - marginalize to only the active qubits,
              - sample shots from that distribution to form classical counts,
              - attach RB metadata (xval, physical_qubits) so analysis works.
        """
        if not isinstance(circuits, (list, tuple)):
            circuits = [circuits]

        shots = int(kwargs.get("shots", getattr(self.options, "shots", 4000)))

        results = []
        n_full = self.processor.num_qubits
        rng = np.random.default_rng()

        for tc in circuits:
            # ---- metadata for RB ----
            meta = dict(getattr(tc, "metadata", {}) or {})

            # xval / length for RB grouping
            if "xval" not in meta:
                L = meta.get("clifford_length", meta.get("length", None))
                if L is None:
                    try:
                        L = int(tc.depth())
                    except Exception:
                        L = None
                if L is not None:
                    meta["xval"] = L

            # physical_qubits for RB
            if "physical_qubits" not in meta:
                try:
                    phys = list(tc.metadata.get("physical_qubits"))
                except Exception:
                    phys = list(range(tc.num_qubits))
                meta["physical_qubits"] = phys

            # Active qubits (for marginalization order)
            try:
                active_idx = [tc.find_bit(q).index for q in tc.qubits]
            except Exception:
                active_idx = list(range(tc.num_qubits))

            # ---- simulate via processor ----
            self.processor.load_circuit(tc)
            rho0 = self.processor.generate_init_processor_state()
            sim_res = self.processor.run_state(rho0)

            rho = self.processor.get_final_circuit_state(sim_res, renorm=False)

            # Diagonal probabilities over full register
            diag = np.real_if_close(np.diag(rho.full()).ravel())
            diag = np.clip(diag, 0.0, 1.0)

            # Marginalize to active qubits: get distribution over bitstrings
            p_act, labels = _marginal_probs_from_diag(diag, n_full, active_idx)
            s = p_act.sum()
            if s > 0:
                p_act = p_act / s
            else:
                p_act = np.ones_like(p_act) / len(p_act)

            # Sample counts
            counts_vec = rng.multinomial(shots, p_act)
            counts = {
                labels[i]: int(c)
                for i, c in enumerate(counts_vec)
                if c > 0
            }

            results.append(
                {
                    "shots": shots,
                    "success": True,
                    "metadata": meta,
                    "header": {"metadata": meta},
                    "data": {"counts": counts},
                }
            )

        qiskit_res = Result.from_dict(
            {
                "backend_name": self.name,
                "backend_version": "0.0.1",
                "job_id": str(uuid.uuid4()),
                "qobj_id": "na",
                "success": True,
                "results": results,
            }
        )
        return _NAJob(self, qiskit_res)


