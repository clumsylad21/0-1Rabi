import numpy as np

# =================== Units & basic helpers ===================

DT = np.complex128
TWOPI = 2.0 * np.pi

def w(MHz):
    """Frequency in MHz -> angular frequency in rad / microsecond."""
    return TWOPI * MHz

def dagger(A):
    """Conjugate transpose."""
    return np.conjugate(A.T)

def op(i, j, d=4):
    """|i><j| in dimension d."""
    M = np.zeros((d, d), dtype=DT)
    M[i, j] = 1.0
    return M


# =================== Single-atom (4-level) primitives ===================

# Basis: |0>, |1>, |r>, |d>
I4 = np.eye(4, dtype=DT)
E00, E11, Err, Edd = op(0, 0), op(1, 1), op(2, 2), op(3, 3)
Er1, E1r = op(2, 1), op(1, 2)          # |r><1|, |1><r|
P_rr_single = Err                      # projector onto |r>

# Pauli-like operators on the { |0>, |1> } subspace, embedded in 4D
X01   = op(0, 1) + op(1, 0)
Z01_4 = op(0, 0) - op(1, 1)


# =================== 1Q Hamiltonians ===================

def H_of_t_Omega01(Omega0_MHz, t0_us, sigma_us):
    """
    Gaussian Rabi drive on the |1> <-> |r> transition,
    implemented as (Ω01(t)/2) * X01 (embedded in 4-level space).
    """
    Omega0 = w(Omega0_MHz)  # rad/us

    def H(t_us):
        Omega_t = Omega0 * np.exp(-((t_us - t0_us) ** 2) / (2.0 * sigma_us ** 2))
        return 0.5 * Omega_t * X01
    return H


def H_detuning_of_t(delta_MHz, T_det_us):
    """
    H(t) = (δ/2) * Z01_4 for 0 <= t < T_det, else 0.
    Used for Z(π) gates via detuning.
    """
    delta = w(delta_MHz)  # rad/us

    def H(t_us):
        if 0.0 <= t_us < T_det_us:
            return 0.5 * delta * Z01_4
        else:
            return np.zeros((4, 4), dtype=DT)
    return H


# =================== 1Q Lindblad jumps (4-level atom) ===================

def lindblad_jumps(gamma_r_per_us, b0=1/16, b1=1/16, bd=7/8):
    """
    Decay r -> {0,1,d} with branching ratios b0,b1,bd.
    gamma_r_per_us is the total decay rate out of |r>.
    """
    L0 = np.sqrt(gamma_r_per_us * b0) * op(0, 2)  # |0><r|
    L1 = np.sqrt(gamma_r_per_us * b1) * op(1, 2)  # |1><r|
    Ld = np.sqrt(gamma_r_per_us * bd) * op(3, 2)  # |d><r|
    return [L0, L1, Ld]


# =================== Generic Lindblad RK4 integrator ===================

def lindblad_rhs(rho, t_us, H_of_t, Ls):
    """
    dρ/dt = -i [H(t), ρ] + Σ_L (LρL† - 1/2 {L†L, ρ})
    rho: (N,N) array, H_of_t(t) -> (N,N), Ls: list of (N,N).
    """
    Ht = H_of_t(t_us)
    comm = -1j * (Ht @ rho - rho @ Ht)

    diss = np.zeros_like(rho, dtype=DT)
    for L in Ls:
        Ld = dagger(L)
        diss += L @ rho @ Ld - 0.5 * (Ld @ L @ rho + rho @ Ld @ L)

    return comm + diss


def evolve_rho(tspan_us, rho0, H_of_t, Ls, steps=4000):
    """
    RK4 integrator for Lindblad dynamics over [t0, t1] in microseconds.
    Returns (ts, trajectory) where trajectory[k] is ρ(t_k).
    """
    t0, t1 = float(tspan_us[0]), float(tspan_us[1])
    ts = np.linspace(t0, t1, steps + 1)
    dt = (t1 - t0) / steps

    rho = rho0.astype(DT)
    traj = [rho.copy()]

    for k in range(steps):
        t = ts[k]
        k1 = lindblad_rhs(rho, t, H_of_t, Ls)
        k2 = lindblad_rhs(rho + 0.5 * dt * k1, t + 0.5 * dt, H_of_t, Ls)
        k3 = lindblad_rhs(rho + 0.5 * dt * k2, t + 0.5 * dt, H_of_t, Ls)
        k4 = lindblad_rhs(rho + dt * k3,       t + dt,       H_of_t, Ls)
        rho = rho + (dt / 6.0) * (k1 + 2*k2 + 2*k3 + k4)

        # enforce Hermiticity (and keep trace ≈ 1)
        rho = 0.5 * (rho + dagger(rho))
        traj.append(rho.copy())

    return ts, np.array(traj, dtype=DT)


# =================== 1Q embed / extract + pulse wrapper ===================

def embed_2x2_to_4x4(rho2):
    """Embed 2x2 computational density matrix into 4x4 (|0>,|1>,|r>,|d>) space."""
    rho4 = np.zeros((4, 4), dtype=DT)
    rho4[:2, :2] = rho2
    return rho4


def extract_2x2_from_4x4(rho4):
    """Project 4x4 single-atom density matrix back onto {|0>,|1>}."""
    return rho4[:2, :2].astype(DT)


def simulate_pulse_return_2x2(Omega0_MHz, sigma_us,
                              t0_us=0.0, gamma_r_inv_us=540.0,
                              steps=4000):
    """
    Module-2 helper:
    Simulates a Gaussian Ω01 pulse starting from |0> and |1>.

    Returns:
        ts, traj0, traj1, pulse_evolve_fn, H_of_t, Ls
      - traj0, traj1 are 4x4 trajectories for initial |0>, |1>
      - pulse_evolve_fn(rho2x2) evolves arbitrary 2x2 state under this pulse
    """
    gamma_r = 1.0 / gamma_r_inv_us  # per microsecond
    Ht = H_of_t_Omega01(Omega0_MHz, t0_us, sigma_us)
    Ls = lindblad_jumps(gamma_r, b0=1/16, b1=1/16, bd=7/8)
    tspan = (t0_us - 6.0 * sigma_us, t0_us + 6.0 * sigma_us)

    rho0 = embed_2x2_to_4x4(np.array([[1, 0],
                                      [0, 0]], dtype=DT))
    rho1 = embed_2x2_to_4x4(np.array([[0, 0],
                                      [0, 1]], dtype=DT))

    ts, traj0 = evolve_rho(tspan, rho0, Ht, Ls, steps=steps)
    _,  traj1 = evolve_rho(tspan, rho1, Ht, Ls, steps=steps)

    def pulse_evolve_fn(rho2x2):
        rho4 = embed_2x2_to_4x4(rho2x2.astype(DT))
        _, tr = evolve_rho(tspan, rho4, Ht, Ls, steps=steps)
        return extract_2x2_from_4x4(tr[-1])

    return ts, traj0, traj1, pulse_evolve_fn, Ht, Ls


# =================== Two-atom helpers (for CZ) ===================

def A(X):
    """Act X on atom A and identity on B."""
    return np.kron(X.astype(DT), I4).astype(DT)

def B_(X):
    """Act X on atom B and identity on A."""
    return np.kron(I4, X.astype(DT)).astype(DT)

P_rr_rr = A(Err) @ B_(Err)   # projector onto |rr>


def lindblad_jumps_twoatom(gamma_r_per_us, b0=1/16, b1=1/16, bd=7/8):
    """
    Two-atom decay channels, each atom decays r -> {0,1,d}.
    """
    L0A = np.sqrt(gamma_r_per_us * b0) * A(op(0, 2))
    L1A = np.sqrt(gamma_r_per_us * b1) * A(op(1, 2))
    LdA = np.sqrt(gamma_r_per_us * bd) * A(op(3, 2))

    L0B = np.sqrt(gamma_r_per_us * b0) * B_(op(0, 2))
    L1B = np.sqrt(gamma_r_per_us * b1) * B_(op(1, 2))
    LdB = np.sqrt(gamma_r_per_us * bd) * B_(op(3, 2))

    return [L0A, L1A, LdA, L0B, L1B, LdB]


def arp_pulses(T_us, Omega_MHz, Delta_MHz, tau_frac):
    """
    Adiabatic rapid passage pulse shapes used in Module 2
    for the CZ gate.

    Returns two callables Omega(t), Delta(t) (rad/us).
    """
    tau = tau_frac * T_us
    a = np.exp(-((T_us / 4) / tau) ** 4)

    Omega0 = w(Omega_MHz)   # rad/us
    Delta0 = w(Delta_MHz)   # rad/us

    def Omega(t):
        if 0.0 <= t <= T_us:
            t0 = T_us / 4 if t < T_us / 2 else 3 * T_us / 4
            shape = (np.exp(-((t - t0) ** 4) / (tau ** 4)) - a) / (1.0 - a)
            return Omega0 * shape
        return 0.0

    def Delta(t):
        if 0.0 <= t <= T_us:
            return -Delta0 * np.cos(TWOPI * t / T_us)
        return 0.0

    return Omega, Delta


def make_H_twoatom(T_us, Omega_MHz, Delta_MHz, tau_frac, B_MHz):
    """
    Build time-dependent 2-atom Hamiltonian H(t) for the ARP CZ gate
    in the full 16-dim space (two 4-level atoms).
    """
    Omega, Delta = arp_pulses(T_us, Omega_MHz, Delta_MHz, tau_frac)
    Bshift = w(B_MHz)

    X01_A = A(Er1 + E1r)
    X01_B = B_(Er1 + E1r)
    PrA   = A(P_rr_single)
    PrB   = B_(P_rr_single)

    def H_of_t(t):
        return (
            0.5 * Omega(t) * (X01_A + X01_B)
            + Delta(t) * (PrA + PrB)
            + Bshift * P_rr_rr
        )

    return H_of_t


def embed_4x4_to_16(r2q):
    """
    Embed 4x4 2-qubit computational density matrix (|00>,|01>,|10>,|11>)
    into the full 16x16 two-atom space, in the upper-left block.
    """
    rho = np.zeros((16, 16), dtype=DT)
    rho[:4, :4] = r2q
    return rho


def extract_4x4_from_16(rho16):
    """
    Project full 16x16 two-atom density matrix onto computational subspace.
    """
    return rho16[:4, :4].astype(DT)

# =================== X / Z / CZ physical channels ===================

def make_X_channel(
    Omega0_MHz=1.0,
    sigma_us=0.20,
    gamma_r_per_us=1/540.0,
    steps=4000,
):
    """
    1-qubit X gate channel from your Module-2 Gaussian π-pulse.

    Returns:
        channel_X(rho2)  # maps 2x2 -> 2x2
    """
    gamma_r_inv_us = 1.0 / gamma_r_per_us

    # This is exactly your Module-2 helper:
    # it builds H(t), Lindblad jumps, and gives a universal evolve_fn
    ts, traj0, traj1, pulse_evolve_fn, Ht, Ls = simulate_pulse_return_2x2(
        Omega0_MHz=Omega0_MHz,
        sigma_us=sigma_us,
        t0_us=0.0,
        gamma_r_inv_us=gamma_r_inv_us,
        steps=steps,
    )

    def channel_X(rho2):
        # rho2 is a 2x2 density matrix on {|0>,|1>}
        return pulse_evolve_fn(rho2)

    return channel_X


def make_Z_channel(
    delta_MHz,
    T_det_us,
    gamma_r_per_us=1/540.0,
    steps=None,
):
    """
    1-qubit Z gate channel from your Module-2 detuning pulse.

    Returns:
        channel_Z(rho2)  # maps 2x2 -> 2x2
    """
    if steps is None:
        steps = max(1000, int(200 * T_det_us))

    Ls = lindblad_jumps(gamma_r_per_us)        # same branching as X pulse
    Ht = H_detuning_of_t(delta_MHz, T_det_us)  # from Module-2

    def channel_Z(rho2):
        # 1) embed into 4-level space |0>,|1>,|r>,|d>
        rho4 = embed_2x2_to_4x4(rho2.astype(DT))

        # 2) evolve under H(t) + Ls
        tspan = (0.0, T_det_us)
        ts, traj = evolve_rho(tspan, rho4, Ht, Ls, steps=steps)

        # 3) project back onto computational subspace
        rho4_final = traj[-1]
        return extract_2x2_from_4x4(rho4_final)

    return channel_Z


def make_CZ_channel(
    two_atom_params_best,
    gamma_r_per_us=1/540.0,
    steps_per_us=300,
):
    """
    2-qubit CZ gate channel from your Module-2 two-atom ARP optimization.

    Args:
        two_atom_params_best: dict from your optimize_params_for_B(...)
            expected keys ~ "T", "Omega", "Delta", "B", "tau_frac"
        gamma_r_per_us: Rydberg decay rate
        steps_per_us: integration resolution; 300 is usually plenty

    Returns:
        channel_CZ(rho4)   # maps 4x4 -> 4x4 on {|00>,|01>,|10>,|11>}
    """
    T_us      = float(two_atom_params_best["T"])
    Omega_MHz = float(two_atom_params_best["Omega"])
    Delta_MHz = float(two_atom_params_best["Delta"])
    B_MHz     = float(two_atom_params_best["B"])
    tau_frac  = float(two_atom_params_best["tau_frac"])

    # Time-dependent 2-atom Hamiltonian in 16-dim full space
    Ht = make_H_twoatom(T_us, Omega_MHz, Delta_MHz, tau_frac, B_MHz)

    # Two-atom Lindblad jumps (each atom decays r -> {0,1,d})
    Ls = lindblad_jumps_twoatom(gamma_r_per_us)

    steps = max(1000, int(steps_per_us * T_us))

    def channel_CZ(rho4):
        """
        rho4 is 4x4 density matrix on {|00>,|01>,|10>,|11>}.

        We:
          - embed into full 16x16 two-atom space,
          - evolve under H(t)+Lindblad,
          - project back to 4x4 computational subspace.
        """
        rho16_0 = embed_4x4_to_16(rho4.astype(DT))

        tspan = (0.0, T_us)
        ts, traj = evolve_rho(tspan, rho16_0, Ht, Ls, steps=steps)
        rho16_f = traj[-1]

        return extract_4x4_from_16(rho16_f)

    return channel_CZ

