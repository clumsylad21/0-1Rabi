def run_and_summarize(exp, backend, label):
    """
    Run a Qiskit Experiments RB/IRB experiment on `backend`,
    block for analysis, and print key analysis results.
    """
    print(f"\n===== {label} =====")
    expdata = exp.run(backend).block_for_results()

    try:
        results = list(expdata.analysis_results())
    except TypeError:
        results = list(expdata.analysis_results() or [])

    for r in results:
        name = getattr(r, "name", "<noname>")
        val  = getattr(r, "value", None)
        print(f"{name:20s} = {val}")

    alpha = next((r.value for r in results if str(r.name).lower() == "alpha"), None)
    epc   = next(
        (r.value for r in results
         if str(r.name).lower() in ("epc", "error_per_clifford")),
        None,
    )
    if alpha is not None:
        print(f"--> alpha ≈ {alpha}")
    if epc is not None:
        print(f"--> EPC   ≈ {epc}")

    return expdata


