def apply_1q_channel_to_full_state(rho_full: Qobj,
                                   q: int,
                                   channel_2x2):
    """
    Apply a CPTP map E on qubit q of an n-qubit density matrix rho_full.
    E is given as a function channel_2x2(rho2) acting on 2x2 matrices.

    We use the same bit-order convention as the marginalization helper below:
      index i -> bits[b] = (i >> (n-1-b)) & 1,  b = 0..n-1
    so qubit 0 is the *leftmost* bit in the computational basis.
    """
    dims = rho_full.dims[0]
    n = len(dims)
    assert all(d == 2 for d in dims), "Only supports qubits."

    R = rho_full.full().astype(np.complex128, copy=True)
    R_out = np.zeros_like(R, dtype=np.complex128)

    def bits_from_index(i):
        # bits[b] = value (0/1) of qubit b, with b=0 the leftmost
        return [(i >> (n - 1 - b)) & 1 for b in range(n)]

    def index_from_bits(bits):
        # bits[b] as above -> integer index
        idx = 0
        for b in range(n):
            idx = (idx << 1) | bits[b]
        return idx

    rest = [b for b in range(n) if b != q]
    k_rest = len(rest)

    for alpha in range(2**k_rest):
        bits_alpha = [0] * n
        # assign rest bits for "bra" index
        for j, b in enumerate(rest):
            # encode alpha MSB-first over rest
            bits_alpha[b] = (alpha >> (k_rest - 1 - j)) & 1

        for beta in range(2**k_rest):
            bits_beta = [0] * n
            # assign rest bits for "ket" index
            for j, b in enumerate(rest):
                bits_beta[b] = (beta >> (k_rest - 1 - j)) & 1

            # build 2x2 block on target qubit q
            block = np.zeros((2, 2), dtype=np.complex128)
            for x in (0, 1):
                bits_alpha[q] = x
                i_idx = index_from_bits(bits_alpha)
                for y in (0, 1):
                    bits_beta[q] = y
                    j_idx = index_from_bits(bits_beta)
                    block[x, y] = R[i_idx, j_idx]

            # apply physical 1Q channel
            block_prime = channel_2x2(block)

            # write back
            for x in (0, 1):
                bits_alpha[q] = x
                i_idx = index_from_bits(bits_alpha)
                for y in (0, 1):
                    bits_beta[q] = y
                    j_idx = index_from_bits(bits_beta)
                    R_out[i_idx, j_idx] = block_prime[x, y]

    return Qobj(R_out, dims=rho_full.dims)


# ---------- helpers for probabilities / marginals ----------

def _bitstrings(k: int):
    """Return all k-bit strings from '0...0' to '1...1'."""
    return [format(i, f"0{k}b") for i in range(2**k)]


def _marginal_probs_from_diag(diag: np.ndarray, n_full: int, keep_indices):
    """
    Sum probabilities over dropped qubits.

    Parameters
    ----------
    diag : np.ndarray
        Diagonal of the full density matrix (length 2**n_full).
    n_full : int
        Total number of qubits in the processor.
    keep_indices : iterable[int]
        Indices of qubits we keep (in the processor's numbering).

    Returns
    -------
    p_keep : np.ndarray
        Marginalized probabilities on the kept qubits.
    labels : list[str]
        Bitstring labels (MSB = smallest qubit index in keep_indices).
    """
    keep = tuple(sorted(int(i) for i in keep_indices))
    k = len(keep)
    if k == n_full:
        return diag.copy(), _bitstrings(k)

    out = np.zeros(2**k, dtype=float)
    for i in range(2**n_full):
        # bits[b] = value of qubit b (0/1), b=0 is leftmost (MSB)
        bits = [(i >> (n_full - 1 - b)) & 1 for b in range(n_full)]
        idx = 0
        for b in keep:
            idx = (idx << 1) | bits[b]
        out[idx] += float(diag[i])

    return out, _bitstrings(k)


