from qiskit.circuit import QuantumCircuit

def make_ansatz(thetas):
    """
    2-qubit ansatz:

    |0,0> --Ry(θ0)
                     │
    |0,1> --Ry(θ1)--CZ--

    thetas: array-like of length 2
    """
    theta0, theta1 = float(thetas[0]), float(thetas[1])
    qc = QuantumCircuit(2)
    qc.ry(theta0, 0)
    qc.ry(theta1, 1)
    qc.cz(0, 1)
    return qc

