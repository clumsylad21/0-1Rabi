def _exp_from_counts_Z0Z1(counts):
    total = sum(counts.values())
    if total == 0:
        return 0.0

    exp = 0.0
    for bitstr, c in counts.items():
        # bitstr is like 'q1 q0' (q1 most significant, q0 least)
        i = int(bitstr, 2)
        b0 = (i >> 0) & 1  # qubit 0
        b1 = (i >> 1) & 1  # qubit 1
        z0 = 1.0 if b0 == 0 else -1.0
        z1 = 1.0 if b1 == 0 else -1.0
        exp += z0 * z1 * c
    return exp / total


def _exp_from_counts_Z0(counts):
    total = sum(counts.values())
    if total == 0:
        return 0.0

    exp = 0.0
    for bitstr, c in counts.items():
        i = int(bitstr, 2)
        b0 = (i >> 0) & 1
        z0 = 1.0 if b0 == 0 else -1.0
        exp += z0 * c
    return exp / total


def _exp_from_counts_Z1(counts):
    total = sum(counts.values())
    if total == 0:
        return 0.0

    exp = 0.0
    for bitstr, c in counts.items():
        i = int(bitstr, 2)
        b1 = (i >> 1) & 1
        z1 = 1.0 if b1 == 0 else -1.0
        exp += z1 * c
    return exp / total

def energy_from_backend(backend, thetas, shots=1024):
    """
    Estimate <H> on a given backend using shots.

    H = -Z0 Z1 - h (X0 + X1)
    """
    thetas = np.array(thetas, dtype=float)
    backend.set_options(shots=shots)

    # --------- <Z0 Z1> ---------
    qc_zz = make_ansatz(thetas)
    qc_zz.measure_all()
    job = backend.run(qc_zz)
    res = job.result()
    counts_zz = res.get_counts()
    exp_ZZ = _exp_from_counts_Z0Z1(counts_zz)

    # --------- <X0> via H on qubit 0 → Z0 ---------
    qc_x0 = make_ansatz(thetas)
    qc_x0.h(0)
    qc_x0.measure_all()
    job = backend.run(qc_x0)
    counts_x0 = job.result().get_counts()
    exp_X0 = _exp_from_counts_Z0(counts_x0)

    # --------- <X1> via H on qubit 1 → Z1 ---------
    qc_x1 = make_ansatz(thetas)
    qc_x1.h(1)
    qc_x1.measure_all()
    job = backend.run(qc_x1)
    counts_x1 = job.result().get_counts()
    exp_X1 = _exp_from_counts_Z1(counts_x1)

    # Combine into energy
    E = -exp_ZZ - h_field * (exp_X0 + exp_X1)
    return float(E)


