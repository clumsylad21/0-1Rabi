def simple_vqe(backend, num_iters=15, init_thetas=None,
               step=0.4, shots=1024, label=""):
    """
     Derivative-free optimizer:
    - Start from init_thetas (random if None)
    - Each iteration: evaluate energy, then try +/- step on each parameter
      and keep the best move.
    - Shrink step slightly each iteration.
    """
    if init_thetas is None:
        init_thetas = 2 * np.pi * np.random.rand(2)
    thetas = np.array(init_thetas, dtype=float)

    history = []
    for it in range(num_iters):
        E = energy_from_backend(backend, thetas, shots=shots)
        history.append(E)
        print(f"[{label}] iter {it:02d}: E = {E:.6f}, thetas = {thetas}")

        # Try local moves on each parameter
        for i in range(len(thetas)):
            best_E = E
            best_theta_i = thetas[i]
            for delta in (+step, -step):
                trial = thetas.copy()
                trial[i] += delta
                E_trial = energy_from_backend(backend, trial, shots=shots)
                if E_trial < best_E:
                    best_E = E_trial
                    best_theta_i = trial[i]
            # Accept improvement
            if best_E < E:
                E = best_E
                thetas[i] = best_theta_i

        # Shrink step slowly
        step *= 0.9

    return thetas, history

