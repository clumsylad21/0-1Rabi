# =================== Neutral-atom Processor using Module-2 gates ===================

class NAProcessor:
    """
    Neutral-atom style processor for Module 4 RB.

    Native ops:
      - ("rx", q, theta)
      - ("rz", q, phi)
      - ("cz", q0, q1)

    Noise model:
      - RX/RZ: Module-2 calibrated Xπ, Zπ physical channels
      - CZ    : Module-2 ARP CZ physical channel
    """
    def __init__(self,
                 num_qubits=2,
                 basis=("rx", "rz", "cz")):
        self.num_qubits = int(num_qubits)
        self.basis = tuple(map(str.lower, basis))
        self.ops = []

        # Neutral-atom parameters
        self.na_params = dict(
            gamma_r_lifetime_us=540.0,  # radiative lifetime
        )
        gamma_r_per_us = 1.0 / self.na_params["gamma_r_lifetime_us"]


        # --- 1Q physical channels ---
        self.X_chan = make_X_channel(
            Omega0_MHz=1.0,
            sigma_us=sigma_pi_us,
            gamma_r_per_us=gamma_r_per_us,
        )
        self.Z_chan = make_Z_channel(
            delta_MHz=delta_opt_MHz,
            T_det_us=T_Z_us,
            gamma_r_per_us=gamma_r_per_us,
        )

        # --- 2Q physical CZ channel ---
        self.CZ_chan = make_CZ_channel(
            two_atom_params_best,
            gamma_r_per_us=gamma_r_per_us,
        )

    # ---------- state prep ----------

    def generate_init_processor_state(self):
        """Return ρ₀ = |0...0><0...0| as a Qobj."""
        ket0 = basis(2, 0)
        psi = ket0
        for _ in range(self.num_qubits - 1):
            psi = tensor(psi, ket0)
        return psi * psi.dag()

    # ---------- circuit loading ----------

    def load_circuit(self, circ):
        """
        Convert a Qiskit QuantumCircuit into a list of native ops.
        Assumes the circuit has been transpiled to rx/rz/cz.
        """
        from qiskit import transpile

        circ_t = transpile(circ, basis_gates=list(self.basis))

        # Map Qiskit qubit objects -> integer indices
        qubit_indices = {qb: i for i, qb in enumerate(circ_t.qubits)}

        ops = []
        for inst, qargs, _ in circ_t.data:
            name = inst.name.lower()
            if name in ("barrier", "measure", "id", "delay"):
                continue

            if name == "rx":
                q = qubit_indices[qargs[0]]
                theta = float(inst.params[0])
                ops.append(("rx", q, theta))
            elif name == "rz":
                q = qubit_indices[qargs[0]]
                phi = float(inst.params[0])
                ops.append(("rz", q, phi))
            elif name == "cz":
                q0 = qubit_indices[qargs[0]]
                q1 = qubit_indices[qargs[1]]
                ops.append(("cz", q0, q1))
            else:
                raise ValueError(f"Non-native gate encountered: {name}")

        self.ops = ops
        return ops

    # ---------- physical gate application ----------

    def _apply_1q_gate_physical(self, rho_full: Qobj,
                            qubit: int,
                            axis: str,
                            angle: float) -> Qobj:
        """
        Apply a noisy 1Q gate on `qubit`:

        ρ  ──>  U_axis(angle) ρ U_axis(angle)†  ──>  physical noise channel

        where the noise channel (X_chan / Z_chan) comes from Module 2.
        """

        axis = axis.lower()
        if axis == "x":
           base_chan = self.X_chan
           def channel_2x2(rho2):
               # Ideal Rx(theta) in Qiskit convention: exp(-i θ X / 2)
               c = np.cos(angle / 2.0)
               s = np.sin(angle / 2.0)
               U = np.array([[c, -1j * s],
                          [-1j * s, c]], dtype=np.complex128)
               rho_rot = U @ rho2 @ U.conj().T
               return base_chan(rho_rot)

        elif axis == "z":
            base_chan = self.Z_chan
            def channel_2x2(rho2):
                # Ideal Rz(phi): exp(-i φ Z / 2)
                e_minus = np.exp(-0.5j * angle)
                e_plus  = np.exp(0.5j * angle)
                U = np.array([[e_minus, 0.0],
                          [0.0,      e_plus]], dtype=np.complex128)
                rho_rot = U @ rho2 @ U.conj().T
                return base_chan(rho_rot)

        else:
            raise ValueError(f"Unknown 1Q axis {axis}, expected 'x' or 'z'.")

        return apply_1q_channel_to_full_state(rho_full, qubit, channel_2x2)


    def _apply_2q_CZ_physical(self, rho_full: Qobj,
                              q0: int,
                              q1: int) -> Qobj:
        """
        Apply the physical 2Q CZ gate from Module-2.

        For simplicity we only support num_qubits == 2 and {q0,q1} = {0,1}.
        """
        if self.num_qubits != 2:
            raise NotImplementedError(
                "Physical CZ implemented only for 2-qubit NAProcessor."
            )
        if sorted((q0, q1)) != [0, 1]:
            raise NotImplementedError(
                "CZ only implemented for qubits (0,1) in this simple version."
            )

        R = rho_full.full().astype(np.complex128, copy=True)  # 4x4
        R_prime = self.CZ_chan(R)  # 4x4 -> 4x4
        return Qobj(R_prime, dims=rho_full.dims)

    # ---------- run & get final state ----------

    def run_state(self, init_state: Qobj):
        """
        Apply all stored operations to init_state and return
        SimpleNamespace(states=[init_state, final_rho]).
        """
        rho = init_state
        for op in self.ops:
            tag = op[0]
            if tag == "rx":
                _, q, theta = op
                rho = self._apply_1q_gate_physical(rho, q, axis="x", angle=theta)
            elif tag == "rz":
                _, q, phi = op
                rho = self._apply_1q_gate_physical(rho, q, axis="z", angle=phi)
            elif tag == "cz":
                _, q0, q1 = op
                rho = self._apply_2q_CZ_physical(rho, q0, q1)
            else:
                raise RuntimeError(f"Unknown op {op}")
        return SimpleNamespace(states=[init_state, rho])

    def get_final_circuit_state(self, result_or_state, renorm: bool = True) -> Qobj:
        """
        Given either the SimpleNamespace from run_state(.) or directly
        a Qobj density matrix, return the final state (optionally renormalized).
        """
        if isinstance(result_or_state, SimpleNamespace):
            rho = result_or_state.states[-1]
        else:
            rho = result_or_state

        if renorm:
            rho = rho / rho.tr()
        return rho

