import numpy as np
import matplotlib.pyplot as plt
from prettytable import PrettyTable
from qiskit import QuantumCircuit, transpile
from qiskit.quantum_info import Statevector
from qutip import Qobj
from na_processor import NAProcessor

# ---------- helpers ----------
def ideal_state_from_circuit(circ: QuantumCircuit) -> np.ndarray:
    sv = Statevector.from_instruction(circ)
    return np.asarray(sv.data, dtype=np.complex128)

def rho_from_na(proc: NAProcessor, circ: QuantumCircuit, *, renorm: bool = True) -> Qobj:
    proc.load_circuit(transpile(circ, basis_gates=list(proc.basis)))
    rho0 = proc.generate_init_processor_state()
    res  = proc.run_state(rho0)
    rhoq = proc.get_final_circuit_state(res, renorm=renorm)
    return rhoq

def pure_state_fidelity(rho, psi: np.ndarray) -> float:
    R = rho.full() if hasattr(rho, "full") else np.asarray(rho, dtype=np.complex128)
    v = np.asarray(psi, dtype=np.complex128).reshape(-1,1)
    return float(np.real((v.conj().T @ R @ v)[0,0]))

def probs_from_rho(rho: Qobj) -> np.ndarray:
    p = np.real(np.diag(rho.full()))
    p = np.clip(p, 0.0, None)
    s = p.sum()
    return (p/s) if s > 0 else p

def probs_from_state(psi: np.ndarray) -> np.ndarray:
    p = np.abs(psi)**2
    return p / p.sum()

def tvd(p, q):  # total variation distance
    return 0.5 * float(np.abs(np.asarray(p)-np.asarray(q)).sum())

def rmse(p, q):
    return float(np.sqrt(np.mean((np.asarray(p)-np.asarray(q))**2)))

def show_bar_compare(p_id, p_na, title="Counts comparison"):
    x = np.arange(len(p_id))
    plt.figure(figsize=(6,3))
    plt.bar(x-0.2, p_id, width=0.4, label="ideal")
    plt.bar(x+0.2, p_na, width=0.4, label="NA")
    plt.xlabel("bitstring (decimal)")
    plt.ylabel("probability")
    plt.title(title); plt.legend(); plt.tight_layout(); plt.show()

# ---------- NA parameters (leakage only in CZ) ----------
default_na = dict(
    T_us=0.54,                 # affects CZ survival s=exp(-T/gamma)
    gamma_r_lifetime_us=540.0, # longer lifetime → less loss
)

# ---------- test circuits ----------
tests = []

# (1) 1q Rx(pi/2)
c = QuantumCircuit(1); c.rx(np.pi/2, 0)
tests.append(("1q Rx(pi/2)", c))

# (2) 1q Rz(pi/2) Rx(pi/2)
c = QuantumCircuit(1); c.rz(np.pi/2, 0); c.rx(np.pi/2, 0)
tests.append(("1q Rz Rx", c))

# (3) 2q Bell: H(0); CX(0,1)
c = QuantumCircuit(2); c.h(0); c.cx(0,1)
tests.append(("2q Bell (H,CX)", c))

# (4) 2q |11> -> CZ
c = QuantumCircuit(2); c.x(0); c.x(1); c.cz(0,1)
tests.append(("2q |11> -> CZ", c))

# (5) 2q random native + CZ
rng = np.random.default_rng(7)
c = QuantumCircuit(2)
for _ in range(4):
    q = int(rng.integers(0,2))
    if rng.random() < 0.6:
        c.rx(float(rng.uniform(-np.pi, np.pi)), q)
    else:
        c.rz(float(rng.uniform(-np.pi, np.pi)), q)
c.cz(0,1)
tests.append(("2q random native + CZ", c))

# ---------- run tests ----------
tbl = PrettyTable(["test", "nq", "trace(NA)", "F(state|renorm)", "TVD", "RMSE"])
for name, circ in tests:
    nq = circ.num_qubits

    proc = NAProcessor(nq, basis=("rx","rz","cz"))
    proc.na_params = default_na.copy()
    rho_na_noren  = rho_from_na(proc, circ, renorm=False)
    rho_na_renorm = rho_from_na(proc, circ, renorm=True)

    psi_id = ideal_state_from_circuit(circ)

    tr  = float(rho_na_noren.tr().real)
    Fid = pure_state_fidelity(rho_na_renorm, psi_id)

    p_id = probs_from_state(psi_id)
    p_na = probs_from_rho(rho_na_renorm)
    Tvd  = tvd(p_id, p_na)
    Rmse = rmse(p_id, p_na)

    tbl.add_row([name, nq, f"{tr:.6f}", f"{Fid:.6f}", f"{Tvd:.4f}", f"{Rmse:.4f}"])

print(tbl)

# ---------- show two distributions ----------
# Bell (should be ~[.5, 0, 0, .5] both ideal and NA)
name, circ = tests[2]
