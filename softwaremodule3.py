rho_na = rho_from_na(proc, circ, renorm=True)
p_id   = probs_from_state(ideal_state_from_circuit(circ))
p_na   = probs_from_rho(rho_na)
show_bar_compare(p_id, p_na, title="Bell: ideal vs NA (probabilities)")

