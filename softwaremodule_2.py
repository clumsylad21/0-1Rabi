def embed_4x4_to_16(r2q):
    rho = np.zeros((16,16), dtype=DT); rho[:4,:4] = r2q
    return rho
def extract_4x4_from_16(r):
    return r[:4,:4].astype(DT)

CZ = np.diag([1,1,1,-1]).astype(DT)

def test_set_2q():
    basis = [np.diag([1,0,0,0]).astype(DT),
             np.diag([0,1,0,0]).astype(DT),
             np.diag([0,0,1,0]).astype(DT),
             np.diag([0,0,0,1]).astype(DT)]
    plus = (DT(0.25) * np.ones((4,4), dtype=DT))  # |++><++|
    return basis, plus

def gate_fids_states_2q(U_target, evolve2q_fn):
    basis, TR = test_set_2q()
    F = []
    for rho_in in basis + [TR]:
        rho_t = U_target @ rho_in @ dagger(U_target)
        rho_i = evolve2q_fn(rho_in)
        F.append(state_fidelity(rho_t, rho_i))
    Far = float(np.mean(F))
    Fg  = float(np.prod(F)) ** (1/5)
    FB, FTR = F[:-1], F[-1]
    prodB = float(np.prod(FB))
    eps = 1e-16
    lam = 1.0 - (1.0 - prodB) / (1.0 - prodB * FTR + eps)
    Fl  = lam * Fg + (1.0 - lam) * Far
    return Far, Fg, Fl

