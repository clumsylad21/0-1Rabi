# ==================== Global numeric knobs ====================
DT = np.complex64           # use np.complex128 for final numbers
TWOPI = 2*np.pi

FAST_STEPS_PER_US  = 350    # dev/tuning
HEAVY_STEPS_PER_US = 1600   # final plots / numbers
FAST_ITERS  = 45
HEAVY_ITERS = 120

def w(MHz): return TWOPI * MHz          # MHz -> rad/us
def dagger(A): return np.conjugate(A.T)

# -------------------- density-matrix fidelities --------------------
def eigh_sqrt_psd(A):
    A = (A + dagger(A)) * DT(0.5)
    ew, V = np.linalg.eigh(A.astype(np.complex128))
    ew = np.clip(ew, 0.0, None)
    return (V*np.sqrt(ew)) @ dagger(V)

def state_fidelity(rho, sigma):
    tr_r2 = np.real(np.trace(rho @ rho))
    tr_s2 = np.real(np.trace(sigma @ sigma))
    if abs(tr_r2-1.0) < 1e-10 and abs(tr_s2-1.0) < 1e-10:
        return float(np.clip(np.real(np.trace(rho @ sigma)), 0.0, 1.0))
    A = eigh_sqrt_psd(rho) @ sigma @ eigh_sqrt_psd(rho)
    Froot = np.real(np.trace(eigh_sqrt_psd(A)))
    F = float(np.clip(Froot, 0.0, None))**2
    return float(np.clip(F, 0.0, 1.0))

# ==================== single-atom primitives ====================
I4 = np.eye(4, dtype=DT)

def op(i,j,d=4):
    M = np.zeros((d,d), dtype=DT); M[i,j] = 1.0; return M

E00, E11, Err, Edd = op(0,0), op(1,1), op(2,2), op(3,3)
Er1, E1r = op(2,1), op(1,2)                         # |r><1|, |1><r|
P_rr_single = Err

def A(X):  return np.kron(X, I4).astype(DT)
def B_(X): return np.kron(I4, X).astype(DT)

P_rr_rr = A(Err) @ B_(Err)

# Fixed Hamiltonian pieces (time dependence enters via scalars Ω(t), Δ(t))
H_OMEGA = 0.5 * (A(Er1 + E1r) + B_(Er1 + E1r))      # couples |1> <-> |r|
H_DELTA = (A(P_rr_single) + B_(P_rr_single))        # Rydberg detuning
def H_CONST_B(B_MHz): return w(B_MHz) * P_rr_rr     # blockade shift on |rr>

# ==================== Lindblad channels (2 atoms) ====================
def lindblad_jumps_twoatom(gamma_r_per_us, b0=1/16, b1=1/16, bd=7/8):
    L0A = np.sqrt(gamma_r_per_us*b0) * A(op(0,2))
    L1A = np.sqrt(gamma_r_per_us*b1) * A(op(1,2))
    LdA = np.sqrt(gamma_r_per_us*bd) * A(op(3,2))
    L0B = np.sqrt(gamma_r_per_us*b0) * B_(op(0,2))
    L1B = np.sqrt(gamma_r_per_us*b1) * B_(op(1,2))
    LdB = np.sqrt(gamma_r_per_us*bd) * B_(op(3,2))
    Ls  = [L0A, L1A, LdA, L0B, L1B, LdB]
    Lds = [dagger(L) for L in Ls]
    LdLs= [Lds[i] @ Ls[i] for i in range(len(Ls))]
    return [L.astype(DT) for L in Ls], [Ld.astype(DT) for Ld in Lds], [LdL.astype(DT) for LdL in LdLs]

# ==================== time-optimal pulses (vectorized) ====================
def timeopt_waves_preset(tg_us, Omega0_MHz, Ddot_MHz_per_us, a_MHz, f_MHz, tau_env_frac, steps):
    """
    Pre-samples Ω(t) and Δ(t) on the grid t_k = k*dt, k=0..steps.
    Returns arrays Om[steps+1], De[steps+1] in rad/us, and mid-point arrays length steps.
    """
    tau_e_us = 0.001825  # 1.825 ns
    t0 = tg_us / 2.0
    dt = tg_us / steps
    ts = np.linspace(0.0, tg_us, steps+1)

    # clip to avoid overflow in exp
    def logistic(x): return 1.0 / (1.0 + np.exp(-np.clip(x, -60.0, 60.0)))

    s_on  = logistic((ts - 20*tau_e_us)/tau_e_us)
    s_off = logistic((tg_us - 20*tau_e_us - ts)/tau_e_us)
    Om = w(Omega0_MHz) * (s_on + s_off - 1.0)              # rad/us

    tau_env = np.clip(tau_env_frac, 0.05, 0.49) * tg_us
    env  = np.exp(-((ts - t0)/tau_env)**4)
    De   = w(Ddot_MHz_per_us) * ts + w(a_MHz) * np.sin(w(f_MHz)*(ts - t0)) * env  # rad/us

    # midpoints for RK4 k2/k3 (linear interpolation)
    Om_mid = 0.5*(Om[:-1] + Om[1:])
    De_mid = 0.5*(De[:-1] + De[1:])

    return dt, Om.astype(np.float64), De.astype(np.float64), Om_mid.astype(np.float64), De_mid.astype(np.float64)

# ==================== batched RK4 (5 states at once) ====================
def rk4_batched(Om, De, Om_mid, De_mid, dt, H_const, Ls, Lds, LdLs, rho_batch):
    """
    Om, De: arrays len N+1; Om_mid, De_mid: len N; dt: scalar; N = number of steps.
    rho_batch: shape (B, 16, 16) complex (B=5 here).
    """
    N = len(Om) - 1
    HΩ = H_OMEGA
    HΔ = H_DELTA

    rho = rho_batch.copy()

    # local helpers using einsum for batched matmuls
    def comm(H, X):  # -i(HX - XH)
        HX = np.einsum('ab,kbc->kac', H, X, optimize=True)
        XH = np.einsum('kab,bc->kac', X, H, optimize=True)
        return -1j*(HX - XH)

    def dissipate(X):
        acc = np.zeros_like(X)
        for L, Ld, LdL in zip(Ls, Lds, LdLs):
            t1 = np.einsum('ab,kbc->kac', L, X,   optimize=True)
            t1 = np.einsum('kab,bc->kac', t1, Ld, optimize=True)
            t2 = np.einsum('ab,kbc->kac', LdL, X, optimize=True)
            t3 = np.einsum('kab,bc->kac', X, LdL, optimize=True)
            acc += t1 - 0.5*(t2 + t3)
        return acc

    for i in range(N):
        # assemble H at t_i, t_{i+1/2}, t_{i+1}
        H1 = (Om[i]    * HΩ + De[i]    * HΔ + H_const).astype(DT, copy=False)
        Hm = (Om_mid[i]* HΩ + De_mid[i]* HΔ + H_const).astype(DT, copy=False)
        H4 = (Om[i+1]  * HΩ + De[i+1]  * HΔ + H_const).astype(DT, copy=False)

        k1 = comm(H1, rho)                  + dissipate(rho)
        k2 = comm(Hm, rho + DT(0.5*dt)*k1) + dissipate(rho + DT(0.5*dt)*k1)
        k3 = comm(Hm, rho + DT(0.5*dt)*k2) + dissipate(rho + DT(0.5*dt)*k2)
        k4 = comm(H4, rho + DT(dt)*k3)     + dissipate(rho + DT(dt)*k3)

        rho = rho + DT(dt/6.0) * (k1 + 2*k2 + 2*k3 + k4)
        rho = (rho + np.conjugate(np.swapaxes(rho, -1, -2))) * DT(0.5)  # enforce hermiticity

    return rho

# ==================== computational subspace plumbing ====================
def embed_4x4_to_16_batch(rhos_4):
    B = rhos_4.shape[0]
    r = np.zeros((B, 16, 16), dtype=DT)
    r[:, :4, :4] = rhos_4
    return r

def extract_4x4_from_16_batch(rhos_16):
    return rhos_16[:, :4, :4].astype(DT)

CZ = np.diag([1,1,1,-1]).astype(DT)

def make_test_set_2q():
    basis = [np.diag([1,0,0,0]).astype(DT),
             np.diag([0,1,0,0]).astype(DT),
             np.diag([0,0,1,0]).astype(DT),
             np.diag([0,0,0,1]).astype(DT)]
    plus = DT(0.25)*np.ones((4,4), dtype=DT)  # |++><++|
    return np.stack(basis + [plus], axis=0)   # shape (5,4,4)

def gate_fids_states_batched(Ut, rho_in_4, rho_out_4):
    # Compute target images
    Ut_d = dagger(Ut)
    target_4 = np.einsum('ab,kbc,cd->kad', Ut, rho_in_4, Ut_d, optimize=True)
    Fs = [state_fidelity(target_4[k], rho_out_4[k]) for k in range(rho_in_4.shape[0])]
    Far = float(np.mean(Fs))
    Fg  = float(np.prod(Fs))**(1/len(Fs))
    prodB = float(np.prod(Fs[:-1])); FTR = Fs[-1]
    lam = 1.0 - (1.0 - prodB)/(1.0 - prodB*FTR + 1e-16)
    Fl  = lam*Fg + (1.0 - lam)*Far
    return Far, Fg, Fl, Fs

# ==================== top-level fidelity (time-opt, batched) ====================
def cz_fidelity_timeopt_batched(tg_us, Om0_MHz, Ddot_MHz_per_us, a_MHz, f_MHz, tau_frac,
                                B_MHz, gamma_r_inv_us=540.0, steps_per_us=FAST_STEPS_PER_US,
                                want_leak=False):
    # precompute waveforms
    steps = max(1000, int(steps_per_us * tg_us))
    dt, Om, De, Om_mid, De_mid = timeopt_waves_preset(
        tg_us, Om0_MHz, Ddot_MHz_per_us, a_MHz, f_MHz, tau_frac, steps
    )
    # jumps
    gamma = 1.0 / gamma_r_inv_us
    Ls, Lds, LdLs = lindblad_jumps_twoatom(gamma)

    # Hamiltonian constant part
    Hc = H_CONST_B(B_MHz)

    # initial 5 states in computational subspace (4x4) -> embed to 16x16 batch
    rho_in_4 = make_test_set_2q()
    rho0 = embed_4x4_to_16_batch(rho_in_4)      # (5,16,16)

    # run batched RK4
    rhoT = rk4_batched(Om, De, Om_mid, De_mid, dt, Hc, Ls, Lds, LdLs, rho0)

    # project back to 4x4 and compute gate fidelities
    rho_out_4 = extract_4x4_from_16_batch(rhoT)
    Far, Fg, Fl, Fs = gate_fids_states_batched(CZ, rho_in_4, rho_out_4)

    leak = None
    if want_leak:
        # leakage = population in any state with at least one |r> (sum of diag outside 4x4 block)
        diag = np.real(rhoT[-2].diagonal())  # (choose |11> index k=3 -> position -2 in our list? we used order [00,01,10,11,++]
        # safer: compute from last batch element corresponding to |11> which is index 3
        diag11 = np.real(rhoT[3].diagonal())
        any_r = 0.0
        for iA in range(4):
            for iB in range(4):
                if iA == 2 or iB == 2:
                    any_r += diag11[iA*4 + iB]
        leak = float(any_r)

    return Far, Fg, Fl, leak

