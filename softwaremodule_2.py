TWOPI = 2.0 * np.pi
def w(MHz):  # frequency (MHz) -> angular freq (rad / microsecond)
    return TWOPI * MHz

def dagger(A): return np.conjugate(A.T)

def op(i, j, d=4):
    """|i><j| in dimension d."""
    M = np.zeros((d, d), dtype=complex); M[i, j] = 1.0
    return M

def eigh_sqrt_psd(A):
    A = 0.5 * (A + dagger(A))
    wv, V = np.linalg.eigh(A)
    wv = np.clip(wv, 0.0, None)
    return (V * np.sqrt(wv)) @ dagger(V)

# Uhlmann state fidelity
def state_fidelity(rho, sigma):
    tr_r2 = np.real_if_close(np.trace(rho @ rho))
    tr_s2 = np.real_if_close(np.trace(sigma @ sigma))
    if abs(tr_r2 - 1.0) < 1e-10 and abs(tr_s2 - 1.0) < 1e-10:
        val = np.real_if_close(np.trace(rho @ sigma))
        return float(np.clip(val, 0.0, 1.0))
    A = eigh_sqrt_psd(rho) @ sigma @ eigh_sqrt_psd(rho)
    Froot = np.real_if_close(np.trace(eigh_sqrt_psd(A)))
    F = float(np.clip(Froot, 0.0, None))**2
    return float(np.clip(F, 0.0, 1.0))

# -------------------- 4-level atom model --------------------
# Basis ordering: |0>, |1>, |r>, |d>
