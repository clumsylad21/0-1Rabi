def cz_fidelity_for_params(T_us=0.54, Omega_MHz=17.0, Delta_MHz=23.0, B_MHz=200.0,
                           tau_frac=0.175, gamma_r_inv_us=540.0,
                           steps_per_us=FAST_STEPS_PER_US, return_leak=False):
    Ht  = make_H_twoatom(T_us, Omega_MHz, Delta_MHz, tau_frac, B_MHz)
    gam = 1.0 / gamma_r_inv_us
    Ls  = lindblad_jumps_twoatom(gam)
    rhs = make_rhs(Ht, Ls)

    steps = max(1000, int(steps_per_us * T_us))

    def evolve2q(rho2):
        r16 = embed_4x4_to_16(rho2)
        _, traj = evolve_rho(0.0, T_us, r16, rhs, steps=steps)
        r2 = extract_4x4_from_16(traj[-1])
        if return_leak:
            diag = np.real(traj[-1, np.arange(16), np.arange(16)])
            any_r = 0.0
            for iA in range(4):
                for iB in range(4):
                    if iA == 2 or iB == 2:
                        any_r += diag[idx(iA,iB)]
            return r2, any_r
        return r2

    if return_leak:
        def eplain(r2): return evolve2q(r2)[0]
        Far, Fg, Fl = gate_fids_states_2q(CZ, eplain)
        leak = evolve2q(np.diag([0,0,0,1]).astype(DT))[1]
        return Far, Fg, Fl, leak
    else:
        return gate_fids_states_2q(CZ, evolve2q)

# =================== Optimization wrappers ===================
def optimize_params_for_B(B_MHz, x0=(0.54, 17.0, 23.0, 0.175),
                          gamma_r_inv_us=540.0, steps_per_us=FAST_STEPS_PER_US,
                          iters_cap=FAST_ITERS):
    hist = []
    def pack(x):
        T  = max(0.05, float(abs(x[0])))
        Om = max(1.0, float(abs(x[1])))
        De = max(1.0, float(abs(x[2])))
        tf = min(0.49, max(0.05, float(abs(x[3]))))
        return T, Om, De, tf

    def obj(x):
        T, Om, De, tf = pack(x)
        Far, Fg, Fl = cz_fidelity_for_params(T, Om, De, B_MHz, tf,
                                             gamma_r_inv_us=gamma_r_inv_us,
                                             steps_per_us=steps_per_us)
        hist.append(Fl)
        return -Fl

    if SCIPY_OK:
        res = minimize(obj, x0=np.array(x0), method="Nelder-Mead",
                       options={"maxiter": iters_cap, "disp": False})
        Tb, Omb, Deb, tfb = pack(res.x)
        Far, Fg, Fl = cz_fidelity_for_params(Tb, Omb, Deb, B_MHz, tfb,
                                             gamma_r_inv_us=gamma_r_inv_us,
                                             steps_per_us=steps_per_us)
    else:
        grid_T  = np.linspace(0.25, 0.8, 6)
        grid_Om = np.linspace(10,  26, 5)
        grid_De = np.linspace(14,  34, 5)
        grid_tf = np.linspace(0.12,0.28,5)
        best = (-1.0, None)
        for T in grid_T:
            for Om in grid_Om:
                for De in grid_De:
                    for tf in grid_tf:
                        Far, Fg, Fl = cz_fidelity_for_params(T, Om, De, B_MHz, tf,
                                                             gamma_r_inv_us=gamma_r_inv_us,
                                                             steps_per_us=steps_per_us)
                        hist.append(Fl)
                        if Fl > best[0]: best = (Fl, (T, Om, De, tf))
        Fl, (Tb, Omb, Deb, tfb) = best
        Far, Fg = None, None

    print(f"[B={B_MHz:>4.0f} MHz] best -> T={Tb:.4f} µs, Ω/2π={Omb:.2f} MHz, Δ/2π={Deb:.2f} MHz, τ/T={tfb:.3f}")
    print("  Flambda={:.6f}{}".format(
        Fl, "" if Far is None else f", Farith={Far:.6f}, Fgeom={Fg:.6f}"))

    plt.figure(figsize=(6,4))
    plt.plot(range(1, len(hist)+1), hist, marker="o")
    plt.xlabel("Iteration"); plt.ylabel("Fidelity (Flambda)")
    plt.title(f"Optimize (T,Ω,Δ,τ/T) at B={B_MHz} MHz")
    plt.grid(True, alpha=0.3); plt.tight_layout(); plt.show()

    return {"B":B_MHz, "T":Tb, "Omega":Omb, "Delta":Deb, "tau_frac":tfb, "Flambda":Fl}

