# ---------- Linear algebra helpers ----------
def dagger(A):  # conjugate transpose
    return np.conjugate(A.T)

def eigh_sqrt(A):
    """Matrix square root for (nearly) PSD Hermitian A."""
    A = 0.5 * (A + dagger(A))              # symmetrize numerically
    w, V = np.linalg.eigh(A)
    w = np.clip(w, 0.0, None)
    return (V * np.sqrt(w)) @ dagger(V)

def density_from_state(psi):
    psi = psi.reshape(-1, 1)
    return psi @ dagger(psi)

# ---------- Test-state set: d basis states + equal superposition ----------
def test_state_set(n_qubits: int):
    d = 2 ** n_qubits
    basis = []
    for i in range(d):
        ket = np.zeros((d, 1), dtype=complex)
        ket[i, 0] = 1.0
        basis.append(ket @ dagger(ket))
    vec = np.ones((d, 1), dtype=complex) / np.sqrt(d)
    TR = vec @ dagger(vec)                 # |+...+><+...+|
    return basis, TR

# ---------- Uhlmann state fidelity ----------
def state_fidelity(rho, sigma):
    # Fast path when both are pure: F = Tr(rho sigma)
    tr_r2 = np.real_if_close(np.trace(rho @ rho))
    tr_s2 = np.real_if_close(np.trace(sigma @ sigma))
    if abs(tr_r2 - 1.0) < 1e-10 and abs(tr_s2 - 1.0) < 1e-10:
        val = np.real_if_close(np.trace(rho @ sigma))
        return float(np.clip(val, 0.0, 1.0))

    A = eigh_sqrt(rho) @ sigma @ eigh_sqrt(rho)
    F_root = np.real_if_close(np.trace(eigh_sqrt(A)))
    F = float(np.clip(F_root, 0.0, None)) ** 2
    return float(np.clip(F, 0.0, 1.0))

def evolve_unitary(U, rho):
    return U @ rho @ dagger(U)

# ---------- Gate fidelities from states (eqs. 5–8) ----------
def gate_fidelities_states(U_target, U_impl, n_qubits=1):
    """Returns Farith, Fgeom, Flambda, per-state list."""
    basis, TR = test_state_set(n_qubits)
    d = 2 ** n_qubits

    F_list = []
    for rho_in in basis + [TR]:
        rho_t = evolve_unitary(U_target, rho_in)
        rho_i = evolve_unitary(U_impl,  rho_in)
        F_list.append(state_fidelity(rho_t, rho_i))

    Farith = float(np.mean(F_list))                          # arithmetic mean
    Fgeom  = float(np.prod(F_list)) ** (1.0 / (d + 1))       # geometric mean

    FB, FTR = F_list[:-1], F_list[-1]                        # basis vs TR term
    prod_basis = float(np.prod(FB))
    eps = 1e-16
    lam = 1.0 - (1.0 - prod_basis) / (1.0 - prod_basis * FTR + eps)  # eq. (8)
    Flambda = lam * Fgeom + (1.0 - lam) * Farith             # eq. (7)

    return Farith, Fgeom, Flambda, F_list

# ---------- Operator-level average gate fidelity for unitaries ----------
def avg_gate_fidelity_unitary(U_target, U_impl):
    d = U_target.shape[0]
    chi = np.trace(dagger(U_target) @ U_impl)
    return (abs(chi) ** 2 + d) / (d * (d + 1))

