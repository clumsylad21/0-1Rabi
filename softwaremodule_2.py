# =================== Lindblad RHS factory (precomputes L† and L†L) ===================
def make_rhs(H_of_t, Ls):
    Lds  = [dagger(L) for L in Ls]
    LdLs = [Lds[i] @ Ls[i] for i in range(len(Ls))]
    def rhs(rho, t_us):
        Ht = H_of_t(t_us)
        comm = -1j * (Ht @ rho - rho @ Ht)
        # use precomputed LdL
        acc = np.zeros_like(rho, dtype=DT)
        for i in range(len(Ls)):
            acc += Ls[i] @ rho @ Lds[i] - DT(0.5) * (LdLs[i] @ rho + rho @ LdLs[i])
        return comm + acc
    return rhs

def evolve_rho(t0, t1, rho0, rhs, steps):
    ts = np.linspace(t0, t1, steps + 1).astype(np.float64)
    dt = (t1 - t0) / steps
    rho = rho0.astype(DT, copy=True)
    out = [rho.copy()]
    for k in range(steps):
        t = ts[k]
        k1 = rhs(rho, t)
        k2 = rhs(rho + DT(0.5*dt) * k1, t + DT(0.5*dt))
        k3 = rhs(rho + DT(0.5*dt) * k2, t + DT(0.5*dt))
        k4 = rhs(rho + DT(dt)      * k3, t + DT(dt))
        rho = rho + DT(dt/6.0) * (k1 + 2*k2 + 2*k3 + k4)
        rho = (rho + dagger(rho)) * DT(0.5)        # clean hermiticity
        out.append(rho.copy())
    return ts, np.asarray(out, dtype=DT)

# =================== Decay channels (two atoms) ===================
def lindblad_jumps_twoatom(gamma_r_per_us, b0=1/16, b1=1/16, bd=7/8):
    L0A = np.sqrt(gamma_r_per_us*b0) * A(op(0,2))
    L1A = np.sqrt(gamma_r_per_us*b1) * A(op(1,2))
    LdA = np.sqrt(gamma_r_per_us*bd) * A(op(3,2))
    L0B = np.sqrt(gamma_r_per_us*b0) * B_(op(0,2))
    L1B = np.sqrt(gamma_r_per_us*b1) * B_(op(1,2))
    LdB = np.sqrt(gamma_r_per_us*bd) * B_(op(3,2))
    return [L0A.astype(DT), L1A.astype(DT), LdA.astype(DT),
            L0B.astype(DT), L1B.astype(DT), LdB.astype(DT)]

# =================== ARP pulses ===================
def arp_pulses(T_us, Omega_MHz, Delta_MHz, tau_frac):
    tau = tau_frac * T_us
    a = np.exp(-((T_us/4)/tau)**4)
    Omega0 = w(Omega_MHz)     # rad/us
    Delta0 = w(Delta_MHz)     # rad/us

    def Omega(t):
        if 0.0 <= t <= T_us:
            t0 = T_us/4 if t < T_us/2 else 3*T_us/4
            shape = (np.exp(-((t - t0)**4)/(tau**4)) - a) / (1.0 - a)
            return Omega0 * shape
        return 0.0

    def Delta(t):
        if 0.0 <= t <= T_us:
            return -Delta0 * np.cos(TWOPI * t / T_us)
        return 0.0

    return Omega, Delta

def make_H_twoatom(T_us, Omega_MHz, Delta_MHz, tau_frac, B_MHz):
    Ω, Δ = arp_pulses(T_us, Omega_MHz, Delta_MHz, tau_frac)
    Bshift = w(B_MHz)

    X01_A = A(Er1 + E1r)
    X01_B = B_(Er1 + E1r)
    PrA   = A(P_rr_single)
    PrB   = B_(P_rr_single)

    def H_of_t(t):
        return DT(0.5) * DT(Ω(t)) * (X01_A + X01_B) + DT(Δ(t)) * (PrA + PrB) + DT(Bshift) * P_rr_rr
    return H_of_t

