# Basis: |0>, |1>, |r>, |d|
Z01_4 = op(0,0) - op(1,1)  # embeds σz on {0,1} into 4x4

def H_detuning_of_t(delta_MHz, T_det_us):
    """H(t) = (δ/2) Z01 for 0<=t<T_det, else 0."""
    delta = w(delta_MHz)  # rad/us
    def H(t_us):
        if 0.0 <= t_us < T_det_us:
            return 0.5 * delta * Z01_4
        else:
            return np.zeros((4,4), dtype=complex)
    return H

def lindblad_jumps(gamma_r_per_us, b0=1/16, b1=1/16, bd=7/8):
    # r -> {0,1,d}; will be inactive here because we never populate |r>
    L0 = np.sqrt(gamma_r_per_us * b0) * op(0,2)
    L1 = np.sqrt(gamma_r_per_us * b1) * op(1,2)
    Ld = np.sqrt(gamma_r_per_us * bd) * op(3,2)
    return [L0, L1, Ld]

def lindblad_rhs(rho, t_us, H_of_t, Ls):
    Ht = H_of_t(t_us)
    comm = -1j * (Ht @ rho - rho @ Ht)
    diss = np.zeros_like(rho, dtype=complex)
    for L in Ls:
        Ld = dagger(L)
        diss += L @ rho @ Ld - 0.5*(Ld @ L @ rho + rho @ Ld @ L)
    return comm + diss

def evolve_rho(t0, t1, rho0, H_of_t, Ls, steps=4000):
    ts = np.linspace(t0, t1, steps+1)
    dt = (t1 - t0)/steps
    rho = rho0.astype(complex)
    traj = [rho.copy()]
    for k in range(steps):
        t = ts[k]
        k1 = lindblad_rhs(rho, t, H_of_t, Ls)
        k2 = lindblad_rhs(rho + 0.5*dt*k1, t + 0.5*dt, H_of_t, Ls)
        k3 = lindblad_rhs(rho + 0.5*dt*k2, t + 0.5*dt, H_of_t, Ls)
        k4 = lindblad_rhs(rho + dt*k3,     t + dt,     H_of_t, Ls)
        rho = rho + (dt/6.0)*(k1 + 2*k2 + 2*k3 + k4)
        rho = 0.5*(rho + dagger(rho))
        traj.append(rho.copy())
    return ts, np.array(traj)

